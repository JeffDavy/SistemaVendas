    private void btn_concluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_concluirMouseClicked
        
        Vendedor vendBean = new Vendedor();
        
        if (verifiaCampos()) {
            
            JOptionPane.showMessageDialog(null, "Preencha todos os campos", "ERRO", JOptionPane.ERROR_MESSAGE);
            
        } else {
            
            vendBean.setNome(txt_nome.getText());
            vendBean.setEmail(txt_email.getText());
            vendBean.setDataNascimento(txt_data.getText());
            vendBean.setCpf(txt_cpf.getText());
            vendBean.setContato1(txt_contato1.getText());
            vendBean.setContato2(txt_contato2.getText());
            vendBean.setUsuario(txt_usuario.getText());
            vendBean.setSenha(txt_senha.getText());
            
            
            if (lbl_operacao.getText().equals("Adicionar Vendedor")) {

                boolean status = vendedorControl.cadastrarController(vendBean);

                if (status) {

                    JOptionPane.showMessageDialog(null, "Vendedor cadastrado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);

                    VendedorArea home = new VendedorArea();
                    home.show();
                    this.dispose();

                } else {

                    JOptionPane.showMessageDialog(null, "Vendedor não cadastrado", "ERRO", JOptionPane.ERROR_MESSAGE);

                    VendedorArea home = new VendedorArea();
                    home.show();
                    this.dispose();

                }
                
            } else if (lbl_operacao.getText().equals("Editar Vendedor")) {
                
                boolean status = vendedorControl.editarController(vendBean);
                
                if (status) {

                    JOptionPane.showMessageDialog(null, "Vendedor editado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);

                    VendedorArea home = new VendedorArea();
                    home.show();
                    this.dispose();

                } else {

                    JOptionPane.showMessageDialog(null, "Vendedor não editado", "ERRO", JOptionPane.ERROR_MESSAGE);

                    VendedorArea home = new VendedorArea();
                    home.show();
                    this.dispose();

                }
                
            } else {
                
                VendedorArea home = new VendedorArea();
                home.show();
                this.dispose();
                
                
            }
            
        }

    }//GEN-LAST:event_btn_concluirMouseClicked

