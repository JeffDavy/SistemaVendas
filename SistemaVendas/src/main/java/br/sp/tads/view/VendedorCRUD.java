    public void preencheCampos(Vendedor vendedorBean) {
                       
        ArrayList<Vendedor> listaVendedor = vendedorControl.consultarController(vendedorBean);

        int i = 0;

        for (Object obj : listaVendedor) {

            Vendedor venBean = (Vendedor) obj;
            
           txt_nome.setText(venBean.getNome());
           txt_data.setText(venBean.getDataNascimento());
           txt_cpf.setText(venBean.getCpf());
           txt_email.setText(venBean.getEmail());
           txt_contato1.setText(venBean.getContato1());
           txt_contato2.setText(venBean.getContato2());
           txt_usuario.setText(venBean.getUsuario());
           txt_senha.setText(venBean.getSenha());
            
           i++;

        }
                       
    }
    
