    public void preencheTabela() {
        
        clienteBean.setEstadoAtual("Ativo");
        
        ArrayList<Cliente> listaCliente = clienteControl.listarController(clienteBean);
        
        if (listaCliente.size() > 0) {
            
            DefaultTableModel tmCliente= new DefaultTableModel();
            
            tmCliente.addColumn("Código");
            tmCliente.addColumn("Nome Fantasia");
            tmCliente.addColumn("Razão Social");
            tmCliente.addColumn("CNPJ");
            tmCliente.addColumn("Contato 1");
            
            tbl_clientes.setModel(tmCliente);
            
            int i = 0;
            
            for (Object obj : listaCliente) {
                
                Cliente cliBean = (Cliente) obj;
                
                tmCliente.addRow(new String[1]);
                
                tbl_clientes.setValueAt(cliBean.getCodCliente(), i, 0);
                tbl_clientes.setValueAt(cliBean.getNomeFantasia(), i, 1);
                tbl_clientes.setValueAt(cliBean.getNome(), i, 2);
                tbl_clientes.setValueAt(cliBean.getCnpj(), i, 3);
                tbl_clientes.setValueAt(cliBean.getContato1(), i, 4);
                
                i++;
                
            }
            
        }
        
    }
    
