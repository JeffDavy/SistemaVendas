    private void btn_removerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_removerMouseClicked
        
        int linhaSelecionada = tbl_clientes.getSelectedRow();
        
        if (linhaSelecionada >= 0) {
            
            int codCliente = Integer.parseInt(tbl_clientes.getValueAt(tbl_clientes.getSelectedRow(), 0).toString());
                       
            clienteBean.setCodCliente(codCliente);
            clienteBean.setEstadoAtual("Offline");
            
            int result = JOptionPane.showConfirmDialog(this, "Deseja exluir o cliente selecionado?", "Excluir", JOptionPane.YES_NO_OPTION);
            
            if (result == JOptionPane.YES_OPTION) {

                if (result == JOptionPane.YES_OPTION) {

                    boolean status = clienteControl.statusController(clienteBean);

                    if (status) {

                        JOptionPane.showMessageDialog(null, "Cliente desativado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                        preencheTabela();

                    } else {

                        JOptionPane.showMessageDialog(null, "Cliente não desativado", "ERRO", JOptionPane.ERROR_MESSAGE);

                    }

                }

            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente", "Erro!", JOptionPane.WARNING_MESSAGE);
            
        }

    }//GEN-LAST:event_btn_removerMouseClicked

